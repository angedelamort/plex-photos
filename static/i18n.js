const I18N_DEFAULT = "en";

function currentLang() {
  const saved = localStorage.getItem("lang");
  if (saved && I18N[saved]) return saved;
  return I18N_DEFAULT;
}

let _lang = currentLang();

// Translate a key, interpolating {placeholders} from params.
function t(key, params) {
  const table = I18N[_lang] || I18N[I18N_DEFAULT];
  let str = (table && table[key]) ?? (I18N[I18N_DEFAULT][key]) ?? key;
  if (params) {
    str = str.replace(/\{(\w+)\}/g, (m, k) => (k in params ? params[k] : m));
  }
  return str;
}

function setLang(lang) {
  if (!I18N[lang]) return;
  _lang = lang;
  localStorage.setItem("lang", lang);
  document.documentElement.lang = lang;
  applyStaticTranslations();
}

function getLang() {
  return _lang;
}

// Apply translations to all elements in the static HTML carrying data-i18n
// attributes. Supports text content, title and placeholder.
function applyStaticTranslations(root) {
  root = root || document;
  root.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = t(node.getAttribute("data-i18n"));
  });
  root.querySelectorAll("[data-i18n-title]").forEach((node) => {
    node.title = t(node.getAttribute("data-i18n-title"));
  });
  root.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    node.placeholder = t(node.getAttribute("data-i18n-placeholder"));
  });
  document.documentElement.lang = _lang;
  document.title = t("app.title");
}
